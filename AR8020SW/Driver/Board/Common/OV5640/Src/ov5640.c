/**
* @brief     
* @param     
* @retval   
* @note   
*/
int32_t OV5640_Init(ENUM_OV5640_FRAME_RATE e_ov5640FrameRate, ENUM_OV5640_MODE e_ov5640Mode)
{
    int32_t s32_result = 0;
    STRU_OV5640_REG_VALUE *pst_ov5640ModeSetting;

    uint32_t u32_ov5640ArSize = sizeof(OV5640_init_setting_30fps_VGA) /
                                sizeof(OV5640_init_setting_30fps_VGA[0]);

    // download the set
    OV5640_DownloadFirmware(OV5640_init_setting_30fps_VGA,u32_ov5640ArSize);

    dlog_info("mode:%d rate:%d",e_ov5640Mode,e_ov5640FrameRate);

    s32_result = OV5640_GetCfgInfoByIndex(e_ov5640Mode, 
                                          e_ov5640FrameRate, 
                                          &pst_ov5640ModeSetting, 
                                          &u32_ov5640ArSize,
                                          &s_st_ov5640Info.u16_ov5640Width,
                                          &s_st_ov5640Info.u16_ov5640Hight,
                                          &s_st_ov5640Info.u8_ov5640FrameRate);
    
    if (0 == s32_result)
    {
        // download mode set
        OV5640_DownloadFirmware(pst_ov5640ModeSetting,u32_ov5640ArSize);
    }

    dlog_info("ov5640 set finished.");
}

